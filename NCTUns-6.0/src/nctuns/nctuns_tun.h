#ifndef __NCTUNS_TUN_H
#define __NCTUNS_TUN_H

#include <sys/types.h>

#define EVENT_TUN_NAME	"ce_tun"

struct tun_event {
	int pid;
	u_int32_t nid;
	int flag;
	u_int64_t value;
	int socket_fd;
};

enum tun_event_flag_enum {
	T0E_TIMEOUT	= 0x01,
	T0E_CHKTUN,
	T0E_CHK_CMDIPC,
	T0E_NOTICE_CHILD_PGID
};

#endif /* __NCTUNS_TUN_H */
